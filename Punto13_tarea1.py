# Punto 13 Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y división.

Numero1= int(input("Ingrese un número"))
Numero2= int(input("Ingrese otro número"))
Suma_Numeros= Numero1 + Numero2
Resta_Numeros= Numero1 - Numero2
Multiplicación_numeros= Numero1 * Numero2
Division_numeros= Numero1 / Numero2
print(f"La suma de los números es: {Suma_Numeros}, la resta de los números es {Resta_Numeros}, la multiplicación de los números es {Multiplicación_numeros} y la división de los números es {Division_numeros}")